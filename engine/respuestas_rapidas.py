def buscar_respuesta_rapida(pregunta: str) -> str | None:
    """
    Busca una respuesta precalculada para la pregunta.
    Retorna None si no encuentra coincidencia.
    
    Sistema mejorado con 3 niveles de b√∫squeda:
    1. Coincidencia exacta
    2. Detecci√≥n por palabras clave (temas cr√≠ticos)
    3. Coincidencia por porcentaje de palabras
    """
    pregunta_lower = pregunta.lower().strip()
    
    # Limpiar caracteres especiales
    pregunta_clean = pregunta_lower.replace("¬ø", "").replace("?", "").replace("¬°", "").replace("!", "").replace("¬∞", "")
    
    # =========================================================================
    # NIVEL 1: B√öSQUEDA EXACTA
    # =========================================================================
    for key, data in RESPUESTAS_RAPIDAS.items():
        for pregunta_template in data["preguntas"]:
            template_clean = pregunta_template.replace("¬ø", "").replace("?", "").replace("¬∞", "").lower()
            if pregunta_clean == template_clean:
                return data["respuesta"]
    
    # =========================================================================
    # NIVEL 2: DETECCI√ìN POR PALABRAS CLAVE (TEMAS CR√çTICOS)
